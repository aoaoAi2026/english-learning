function generateMemoryTip(text, meaning) {
  return [
    { type: 'association', content: `${text} → ${meaning}的英文` }
  ];
}

function generateNewWords(gradeId, startId, words) {
  return words.map((w, i) => {
    const id = `${gradeId}-w${startId + i}`;
    const [example, exampleMeaning] = generateExample(w.text, w.meaning);
    const text = w.text;
    const upperText = text.charAt(0).toUpperCase() + text.slice(1);
    return {
      id,
      text,
      phonetic: w.phonetic,
      meaning: w.meaning,
      example: example || `${upperText} is a common word.`,
      exampleMeaning: exampleMeaning || `${w.meaning}是一个常用词。`,
      unit: w.unit,
      memoryTips: generateMemoryTip(text, w.meaning),
    };
  });
}

