for (const [key, config] of Object.entries(gradeMap)) {
  const data = newWords[key];
  if (!data) continue;

  const filePath = path.join(__dirname, '..', 'src', 'data', 'words', config.file);
  let content = fs.readFileSync(filePath, 'utf8');

  const words = generateNewWords(config.gradeId, data.startId, data.words);
  const wordsStr = words.map(w => {
    const safeExample = w.example.replace(/['']/g, "'");
    const safeExampleMeaning = w.exampleMeaning.replace(/['']/g, "'");
    const safeMeaning = w.meaning.replace(/['']/g, "'");
    return `  {
    id: '${w.id}',
    text: '${w.text}',
    phonetic: '${w.phonetic}',
    meaning: '${safeMeaning}',
    example: '${safeExample}',
    exampleMeaning: '${safeExampleMeaning}',
    unit: '${w.unit}',
    memoryTips: [
      { type: 'association', content: '${w.text} \\u2192 ${safeMeaning}' }
    ]
  }`;
  }).join(',\n');

  // Insert before closing ];
  content = content.replace(/\n\];$/, `,\n${wordsStr}\n];`);

  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`✅ ${config.file}: added ${words.length} words (total with ~${newWords[key].startId + words.length} entries)`);
}
